package coreJava_Class;

public class CharConcept_Dec11 {

	public static void main(String[] args) {

		int a=10;
		int b=20;
		System.out.println(a+b);//30
		System.out.println(a-b);//30
		System.out.println(a*b);//30
		System.out.println(a/b);//30
		
		//ASCII Values
		//a-z:
		//A-Z:
		//0-9:
		char c1='a';
		char c2='b';
		System.out.println(c1);//a
		System.out.println(c1+c2);//97+98
		System.out.println(c1-c2);//-1
		System.out.println('a'+'b');//195
		System.out.println('a'+0);//97
		System.out.println('a'+'0');//145
		
		char c=' ';
		System.out.println((float)c); //ASCII:32
		
		int q=6;
		System.out.println((char)q);
//		char c1=' ';
//		System.out.println((float)c1); //ASCII:32
		
		
		
	}

}
